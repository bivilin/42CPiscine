int	ft_iterative_factorial(int nb)
{
	int r;

	r = nb;
	if (nb > 0 && nb < 13)
	{
		while (nb != 1)
		{
			r = r * (nb - 1);
			nb--;
		}
		return (r);
	}
	else if (nb == 0)
		return (1);
	else
		return (0);
}
