int	ft_atoi(char *str)
{
	int i;
	int out;
	int neg;

	i = 0;
	out = 0;
	neg = 1;
	while (str[i] <= ' ')
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			neg = -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		out = out * 10 + str[i] - '0';
		i++;
	}
	return (out * neg);
}
